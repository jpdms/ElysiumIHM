    private static void envoiTCP(DataOutputStream outTCP, String trame) throws Exception {
        trame += "\n";
        byte[] b = trame.getBytes();            // il faut envoyer à bas niveau
        outTCP.write(b, 0, trame.length());
        outTCP.flush();
        System.out.println(trame);
        Thread.sleep(100);
    }
    
    private static String receptionUDP(DatagramSocket serveurUDP) throws IOException {
        byte[] inUDP = new byte[1024];
        DatagramPacket receivePacket = new DatagramPacket(inUDP, inUDP.length);
        // ------------------- reception bloquante ---------------------------
        serveurUDP.receive(receivePacket);
        String recu = new String( receivePacket.getData());
        recu = recu.substring(0, 3);
        return recu;
    }
    
    private static String replacePart(String trame, int start, int length, int valeur) {
        String out = "00000000";
        out += Integer.toHexString(valeur);
        out = out.substring(out.length() - length);
        return trame.substring(0, start) + out + trame.substring(start+length, trame.length());
    }
